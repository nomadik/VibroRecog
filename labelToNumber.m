function y = labelToNumber(val)
    if (strcmp(val, 'Ball'))
        y = 1;
    elseif (strcmp(val, 'Cott'))
        y = 2;
    elseif (strcmp(val, 'Pill'))
        y = 3;
    elseif (strcmp(val, 'Salt'))
        y = 4;
    elseif (strcmp(val, 'Soda'))
        y = 5;
    elseif (strcmp(val, 'Spic'))
        y = 6;
    elseif (strcmp(val, 'Spou'))
        y = 7;
    end;
end
        
