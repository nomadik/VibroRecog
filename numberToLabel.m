function y = numberToLabel(val)
    if (val == 1)
        y = 'Ball';
    elseif (val == 2)
        y = 'Cott';
    elseif (val == 3)
        y = 'Pill';
    elseif (val == 4)
        y = 'Salt';
    elseif (val == 5)
        y = 'Soda';
    elseif (val == 6)
        y = 'Spic';
    elseif (val == 7)
        y = 'Spou';
    end;
end
        
